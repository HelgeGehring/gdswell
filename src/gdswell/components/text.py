@cell
def text_char(char: str, layer: Layer, size: float = 1.0) -> Cell:
    """
    Renders a single character on the specified layer.
    Monospace 5x7 bitmap font.

    Args:
        char: The character to render.
        layer: The layer to render on.
        size: Height of the character in microns.
    """
    c = Cell()
    if char not in FONT:
        # Fallback to a square or '?'
        if "?" in FONT:
            char = "?"
        else:
            return c

    bitmap = FONT[char]
    pixel_size = size / 7.0

    for row_idx, row_val in enumerate(bitmap):
        # row_idx 0 is top
        y = (6 - row_idx) * pixel_size
        for col_idx in range(5):
            # col_idx 0 is left (MSB in our 5-bit convention if we think 0x10 is left)
            # wait, let's check: 0x10 is 16, binary 10000.
            # If 0x10 is left, then (4 - col_idx) bit
            if (row_val >> (4 - col_idx)) & 1:
                x = col_idx * pixel_size
                # Add rectangle
                pts = [
                    (x, y),
                    (x + pixel_size, y),
                    (x + pixel_size, y + pixel_size),
                    (x, y + pixel_size),
                ]
                c.add_polygon(pts, layer)

    return c


@cell
def text(
    text: str,
    layer: Layer,
    size: float = 1.0,
    spacing: float = 1.1,
    line_spacing: float = 1.5,
) -> Cell:
    """
    Renders a string of text, supporting multiple lines with \n.

    Args:
        text: The string to render.
        layer: The layer to render on.
        size: Height of the characters in microns.
        spacing: Character spacing multiplier (default 1.1 -> 10% gap).
        line_spacing: Vertical spacing multiplier (default 1.5).
    """
    c = Cell()
    char_width = size * (5.0 / 7.0)

    lines = text.split("\n")
    for row, line in enumerate(lines):
        y_offset = -row * size * line_spacing
        for col, char in enumerate(line):
            if char == "\n" or char == "\r":
                continue
            char_cell = text_char(char=char, layer=layer, size=size)
            c.add_ref(char_cell, origin=(col * char_width * spacing, y_offset))

    return c
